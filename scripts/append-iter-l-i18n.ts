for (const locale of Object.keys(translations) as Locale[]) {
  const path = `src/i18n/locales/${locale}.ts`;
  const src = readFileSync(path, "utf8");
  const dict = translations[locale];
  // Verify all required keys present
  for (const k of keys) {
    if (dict[k] == null) throw new Error(`missing ${locale}:${k}`);
  }
  const block = keys
    .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(dict[k])},`)
    .join("\n");

  // Find closing marker.
  // es.ts uses `} as const;` (esDict). Others use `};` at end of object.
  let updated: string;
  if (locale === "es") {
    updated = src.replace(/(\n)(} as const;)/, `\n${block}\n$2`);
  } else {
    // Insert before the final `};`
    updated = src.replace(/(\n)(};[\s\n]*)$/, `\n${block}\n$2`);
  }
  if (updated === src) throw new Error(`no insertion for ${locale}`);
  writeFileSync(path, updated);
  console.log(`✓ ${locale}: +${keys.length} keys`);
}
